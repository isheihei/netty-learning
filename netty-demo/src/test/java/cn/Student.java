package cn;

public class Student {
    String name;
    int age;

    public Student() {
        this.name = name;
        this.age = age;
    }

    public void say(){
        System.out.println("Student");
    }
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
